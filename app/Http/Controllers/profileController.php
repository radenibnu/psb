    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'password'  => 'confirmed',
        ]);

        $user = User::where('id', Auth::user()->id)->first();
        $user->name = $request->name;
        $user->email = $request->email;
        // $user->nohp = $request->nohp;
        // $user->alamat = $request->alamat;
